  $(document).ready(function () {
    var images = [
      'img/E1.jpg', 'img/E2.jpg', 'img/E3.jpg', 'img/E4.jpg', 'img/E5.jpg',
      'img/E6.jpg', 'img/E7.jpg', 'img/E8.jpg', 'img/E9.jpg', 'img/E10.jpg',
      'img/E11.jpg', 'img/E13.jpg'
    ];
    var itemsPerPage = 4;
    function renderGallery(page) {
      var start = (page - 1) * itemsPerPage;
      var end = start + itemsPerPage;
      var galleryHtml = '';
      for (var i = start; i < end && i < images.length; i++) {
        galleryHtml += '<li><a href="#" style="background: url(' + images[i] + '); background-size: cover;" aria-controls="aside" aria-expanded="false"></a></li>';
      }
      $('#gallery-list').html(galleryHtml);
    }
    $('.page-btn').on('click', function () {
      var page = $(this).data('page');
      renderGallery(page);
    });
    renderGallery(1);
  });
